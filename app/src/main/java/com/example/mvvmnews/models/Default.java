package com.example.mvvmnews.models;

public class Default {

    private String kind;
    private Data data;

    public Default() {
    }

    public Default(String kind) {
        this.kind = kind;
        this.data = data;
    }

    public String getKind() {
        return kind;
    }

    public void setKind(String kind) {
        this.kind = kind;
    }

    public Data getData() {
        return data;
    }
}
